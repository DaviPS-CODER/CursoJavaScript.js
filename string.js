const escola = "coder"

console.log(escola.charAt(4)) //retorna a letra que está na posição 4

console.log(escola.charAt(5)) // em JS, quando não tem, retorna string vazia

console.log(escola.charCodeAt(3)) // encontra na tabela unicode

console.log(escola.indexOf("o")) // diz em qual posição está aquele elemento da string

console.log(escola.substring(1)) // vai imprimir a partir da posição 1

console.log(escola.substring(0,3)) // vai imprimir da posição 0 a 3, sem incluir o 3

console.log(escola.replace(3, "3")) // substitui o elemento na posição 3 pela string

console.log("ana, maria, pedro".split(",")) // gera um array com os elementos da string, os dividindo pelo ponto

